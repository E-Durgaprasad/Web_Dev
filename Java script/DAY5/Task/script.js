const button = document.getElementById("btn");
const box = document.getElementById("box");

const colors = [
    "#FF5733",
    "#33FF57",
    "#3357FF",
    "#F1C40F",
    "#8E44AD",
    "#1ABC9C"
];

let index = 0;

button.addEventListener("click", function () {
    box.style.backgroundColor = colors[index];
    index++;

    if (index >= colors.length) {
        index = 0;
    }
});














































































































































































